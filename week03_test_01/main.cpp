#if defined(__APPLE__)                              // macOS 환경이면 Apple GLUT 헤더를 사용한다.
#include <GLUT/glut.h>                              // macOS 기본 GLUT 프레임워크 헤더를 포함
#else                                               // macOS가 아닌 환경 분기
#include <GL/glut.h>                                // 일반 OpenGL GLUT 헤더 포함
#endif

#include <iostream>                                 // 콘솔 출력을 위한 C++ 표준 라이브러리
#include <stdio.h>                                  // C 표준 입출력 (기존 코드 유지)

// 화면에 그래픽을 그리는 핵심 콜백 함수
void RenderScene(void)
{
    std::cout << "RenderScene" <<std::endl;        // 렌더 함수가 호출될 때마다 콘솔에 출력
    glClear(GL_COLOR_BUFFER_BIT);                   // SetupRC에서 설정한 clear color(파란색)로 화면 버퍼를 지움

    // [View(뷰포트) 설정] : 가상 도화지에서 그려진 결과물을 실제 윈도우 창의 구체적인 '어느 픽셀 영역'에 보여줄지 정함.
    // glViewport(시작 x, 시작 y, 폭 width, 높이 height)
    // (0, 0) 즉 창의 왼쪽 아래 구석부터 폭 640, 높이 480 픽셀 영역 전체를 사용하겠다는 의미.
    glViewport(0, 0, 640, 480);

    glColor3f(1.0f, 0.0f, 0.0f);                    // 이후에 그려지는 면/선/점의 색상을 빨간색(R:1, G:0, B:0)으로 설정
    
    // 사각형 그리기 
    // 아래 SetupRC()에서 Projection이 직교(Ortho)로 가로 -320~320, 세로 -240~240 로 맞춰져 있습니다.
    // 따라서 화면 정중앙이 원점(0,0)입니다.
    // x좌표 0부터 320(우측 전체), y좌표 0부터 240(상단 전체)을 두르는 직사각형이 그려집니다.
    // 최종적으로 화면 기준 우상단 1사분면에 빨간 네모가 가득 차게 나옵니다.
    glRectf(0, 240, 320, 0); 

    // 더블 버퍼링에서 백 버퍼(Back Buffer)에 다 그린 그림을 프론트 버퍼(실제 보이는 화면)와 교체함
    glutSwapBuffers();
}

// 렌더링 컨텍스트(Rendering Context) 초기값 설정 함수
void SetupRC(void)
{
    std::cout << "SetupRC" <<std::endl;            // 초기화 진행됨을 알리는 콘솔 메시지
    
    // glClear(지우기)를 명령할 때 도화지를 ಯಾವ 색상으로 덮어버릴지 '초깃값 상태'를 지정.
    // 여기서는 R:0.0, G:0.0, B:1.0, A:1.0 이므로 파란색 바탕을 설정함.
    glClearColor(0.0f, 0.0f, 1.0f, 1.0f); 

    // [Perspective / Projection (투영) 설정] 
    // 우리가 사용할 "가상 좌표계의 범위"와 "카메라 렌즈의 성격(원근 유무 등)"을 정의하는 부분.
    glMatrixMode(GL_PROJECTION);                    // "지금부터 투영행렬(렌즈/카메라 옵션) 설정을 건드리겠다"고 선언
    glLoadIdentity();                               // 기존 투영행렬에 누적되어있던 값들을 깨끗한 단위행렬로 초기화 (필수)

    // 직교 투영(Orthographic Projection) 좌표계 설정. 멀고 가까움에 따른 크기 변화(원근감)가 없는 평행 영사 렌즈임.
    // glOrtho(left, right, bottom, top, near, far)
    // 화면 왼쪽 끝을 x=-320, 우측 끝을 x=+320 으로 매핑. (총 폭 640 단위)
    // 화면 하단 끝을 y=-240, 상단 끝을 y=+240 으로 매핑. (총 높이 480 단위)
    // -> 현재 창 픽셀 크기(640x480)와 좌표계의 스케일을 1:1로 일치시켜 직관적으로 그릴 수 있게 해둠.
    glOrtho(-320, 320, -240, 240, -1, 1); // 여기가 -1,1,-1,1,-1,1 이면 NDC 기준으로 -1~1 범위가 창 전체에 매핑되므로, glRectf(-0.5, -0.5, 0.5, 0.5) 같은 명령이 창 중앙에 작은 사각형을 그리는 형태로 해석됨. 화면에는 잘 나오는 것처럼 보여도 결국 이게 화면에 나오는 좌표계의 범위를 결정하는 부분이므로, 창 크기에 맞춰서 glOrtho의 범위를 조절해주는 것이 중요함. 지금 상태는 창이 2x2 크기임

    // 투영(렌즈) 세팅이 끝났으니 행렬 모드를 모델(물체위치)과 뷰(카메라위치 관측)를 다루는 모드로 원복
    glMatrixMode(GL_MODELVIEW);                     
    glLoadIdentity();                               // 모델 뷰 행렬 역시 이전 찌꺼기가 남지 않게 초기화 해둠.
}

int main(int argc, char** argv)
{
    glutInit(&argc, argv);                          // GLUT 시스템 초기화

    glutInitDisplayMode(GLUT_DOUBLE | GLUT_RGB);    // RGB 컬러 모델과 부드러운 애니메이션을 위한 더블버퍼 모드를 활성화
    
    glutInitWindowSize(640, 480);                   // 창(Window)의 크기를 가로 640 픽셀, 세로 480 픽셀로 설정
    glutInitWindowPosition(500, 500);               // 모니터 화면 중 x:500, y:500 픽셀 위치에 창을 생성
    glutCreateWindow("week03_test_01");             // 제목 표시줄 이름이 "week03_test_01"인 실제 프로그램 윈도우 생성

    SetupRC();                                      // 위에서 정의한 화면 배경색/투영 좌표계 등 초기 상태 설정 실행
    glutDisplayFunc(RenderScene);                   // 화면을 다시 그려야 할 때(예: 처음 떴을 때) 호출할 콜백 함수(RenderScene) 지정

    glutMainLoop();                                 // 무한 루프 진입. 이 함수는 종료되지 않으며, 사용자 이벤트가 생기면 적절한 콜백을 호출함
    
}

/* 
================================================================================
  [전체적인 흐름 및 View / Perspective 개념 요약 정리]
================================================================================

1. 전체적인 흐름 (Program Flow)
  - 시작 (main 함수): 
    1) glutInit, glutInitWindowSize 등을 통해 가로 640, 세로 480 크기의 실제 OS 창을 띄웁니다.
    2) SetupRC()를 호출하여 프로그램 시작 시 단 한 번 필요한 초기 렌더링 상태를 지정합니다. (예: “앞으로 배경 지울 때는 파란색으로 밀어라”, “렌즈는 이런 직교렌즈를 쓸 것이다” 등)
    3) glutDisplayFunc()로 "그리기용 함수가 무엇인지" 시스템에 알려줍니다. (여기서는 RenderScene)
    4) glutMainLoop()라는 이벤트 무한 반복 대기 상태로 들어갑니다.

  - 콜백 동작 및 그리기 (RenderScene 함수):
    1) 운영체제가 '창을 새로 갱신해야 해'라고 할 때마다 RenderScene()이 실행됩니다.
    2) 제일 먼저 glClear 로 이전의 복잡한 잔상을 앞서 약속한 파란 배경색으로 깨끗이 밀어버립니다.
    3) 빨간색(glColor3f)을 골라서, 우상단(glRectf)에 사각형을 조공합니다. 
    4) 백버퍼(보이지 않는 도화지)에 다 그린 뒤, glutSwapBuffers()로 휙 뒤집어 사용자 눈(모니터)에 실시간으로 그림을 보여줍니다.


2. View (뷰포트 설정) -> "도면의 결과를 최종적으로 창의 어느 위치에 매핑 표시할 것인가!"
  - 주요 함수: glViewport(x, y, width, height)
  - OpenGL 파이프라인에서 '투영'을 마친 정규화된 그림(보통 -1~1 범위)을, 모니터 상의 **실제 픽셀 크기**를 가진 영역에 입히는 단계입니다.
  - 예를 들어 창 크기가 640x480 인데 뷰포트도 (0, 0, 640, 480) 이라면 창 전체에 그림이 딱 맞게 늘어납니다.
  - 창 크기는 640x480 인데 뷰포트를 (0, 0, 320, 240) 으로 절반만 설정하면, 화면 왼쪽 아래 구석에만 결과물이 축소되어 밀집되어 표시됩니다.


3. Perspective / Projection (원근 & 투영 설정) -> "어떤 렌즈로, 좌표계는 어떻게 잡고 훔쳐볼 것인가!"
  - 주요 함수: glMatrixMode(GL_PROJECTION), glOrtho(직교 투영), gluPerspective(원근 투영)
  - OpenGL에서는 우리가 가상의 머릿속 공간에 그린 좌표를 어떻게 '카메라 필름'으로 눌러 담을지를 '투영 변환(Projection Matrix)'에 곱해 계산합니다.
  - 본 코드의 glOrtho(-320, 320, -240, 240, -1, 1)의 의미:
    * "지금 내가 그릴 좌표 공간의 가로 길이는 왼쪽 끝이 -320 이고, 오른쪽 끝이 320 이야." (가로 범위 총 640)
    * "아래쪽 끝은 -240 이고, 높이 상단 끝은 240 이야." (세로 범위 총 480)
  - 즉, 내가 설정한 윈도우 크기(640x480)의 픽셀 개수와 1:1로 단위가 매치되는 2D 직교 좌표계를 만든 것입니다!
  - 그래서 glRectf(0, 240, 320, 0) 이라고 명령 내렸을 때 직관적으로 "x : 중앙(0) ~ 맨 우측 끝(320) / y : 중앙(0) ~ 맨 위측 끝(240)" 을 지칭하게 되어 1사분면(우상단)에 정확히 그려지게 됩니다.
  - 여기에 만약 3D적인 카메라 원근감(가까운건 크고 먼건 작은 현상)을 추가하려면 직교투영인 glOrtho 대신 gluPerspective 같은 원근투영 함수를 투영 행렬(GL_PROJECTION) 모드에서 설정해주게 됩니다.

4. glOrtho (투영 설정) 호출 위치에 대한 고찰 (SetupRC vs RenderScene)
  - 결론: glOrtho를 SetupRC가 아닌 RenderScene에 넣어도 기능상 출력 결과는 완전히 동일합니다. (그리기 직전에만 상태가 설정되어 있으면 됨)
  - SetupRC 내부 설정: 창 크기나 좌표계가 "고정"일 경우 유리. 시작 시 한 번만 설정하므로 행렬 계산 연산 낭비가 없습니다.
  - RenderScene 내부 설정: 매 프레임(초당 수십 번) 투영 행렬을 초기화하고 재계산합니다. 고정된 뷰에서는 낭비지만, 실시간으로 투영 범위가 변해야 할 때 유용합니다.
  - 리쉐이프(Reshape) 콜백: 실무에서 가장 권장되는 방식입니다. 사용자가 윈도우 창 크기를 조절할 때만 호출되는 glutReshapeFunc()를 만들어, 그 안에서 glViewport와 투영(glOrtho/gluPerspective)을 재설정하면 창 찌그러짐을 예방하며 성능도 아낄 수 있습니다.
================================================================================
*/

